# Day 2 - If Else

# Example 1
num = 10
if num > 5:
  print("Number is greater than 5")
else:
  print("Number is less than or equal to 5")

# Example 2
num = 7
if num % 2 == 0:
  print("The number is even")
else:
  print("The number is odd")
  
# Example 3 - Positive or Negative using If Else

num = int(input("Enter a number:"))
if num > 0:
  print("Number is positive")
else:
  print("Number is negative")


# Expected Output
# Number is greater than 5

# Expected Output
# The number is odd

# Expected Output
# Enter a number: -3
# Number is negative
